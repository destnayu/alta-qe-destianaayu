class Operasi {
    int n1, n2, hitung;

    public void getPenjumlahan() {
        hitung = n1 + n2;
        System.out.println(+n1+ " + " +n2+ " = " + hitung);
    }
    public void getPengurangan() {
        hitung = n1 - n2;
        System.out.println(+n1+ " - " +n2+ " = " + hitung);
    }
    public void getPerkalian() {
        hitung = n1 * n2;
        System.out.println(+n1+ " * " +n2+ " = " + hitung);
    }
    public void getPembagian() {
        hitung = n1 / n2;
        System.out.println(+n1+ " / " +n2+ " = " + hitung);
    }
}
